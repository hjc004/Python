tuple_one = ('Google', 'Runoob', 'Taobao')

# 计算元组元素个数
len_size = len(tuple_one)
print(len_size)

# 返回元组元素最大值和最小值
tuple_two = ('5', '4', '8')
max_size = max(tuple_two)
min_size = min(tuple_two)
print(max_size)
print(min_size)

# 将列表转为元组
list_demo = ['Google', 'Taobao', 'Runoob', 'Baidu']
tuple_three = tuple(list_demo)
print(tuple_three)
